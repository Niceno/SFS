!======================================================================!
  subroutine Print_Vector(message, vector)
!----------------------------------------------------------------------!
  implicit none
!----------------------------------------------------------------------!
  character(len=*)   :: message
  real, dimension(:) :: vector
!----------------------------------------------------------------------!
  integer :: row
!----------------------------------------------------------------------!

  write(*,*) message

  do row = 1, size(vector)
    write(*,"(f8.3)") vector(row)
  end do           

  end subroutine Print_Vector

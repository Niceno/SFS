!======================================================================!
  subroutine Print_Vector(a, n)
!----------------------------------------------------------------------!
  implicit none
!----------------------------------------------------------------------!
  integer            :: n
  real, dimension(n) :: a
!----------------------------------------------------------------------!
  integer :: row, col
!----------------------------------------------------------------------!

  do row = 1, n
    write(*,"(f8.3)") a(row)
  end do           

  end subroutine Print_Vector

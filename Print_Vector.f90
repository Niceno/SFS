!======================================================================!
  subroutine Print_Vector(a)
!----------------------------------------------------------------------!
  implicit none
!----------------------------------------------------------------------!
  real, dimension(:) :: a
!----------------------------------------------------------------------!
  integer :: row, col
!----------------------------------------------------------------------!

  do row = 1, size(a)
    write(*,"(f8.3)") a(row)
  end do           

  end subroutine Print_Vector

!==============================================================================!
  subroutine Square_Mod_Allocate(a, n)
!------------------------------------------------------------------------------!
!   Deallocates memory occupied by Square_Type                                 !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  type(Square_Type) :: a
  integer           :: n
!------------------------------------------------------------------------------!

  a % n = n
  allocate(a % val(n,n))    ! matrix entries
  a % val(:,:) = 0.0

  end subroutine

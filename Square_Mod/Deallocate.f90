!==============================================================================!
  subroutine Square_Mod_Deallocate(a)
!------------------------------------------------------------------------------!
!   Deallocates memory occupied by Square_Type                                 !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  type(Square_Type) :: a
!------------------------------------------------------------------------------!

  a % n = 0
  deallocate(a % val)    ! matrix entries

  end subroutine

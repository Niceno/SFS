!======================================================================!
  subroutine Transpose_Matrix(b, a, n)
!----------------------------------------------------------------------!
  implicit none
!----------------------------------------------------------------------!
  integer              :: n
  real, dimension(n,n) :: b
  real, dimension(n,n) :: a
!----------------------------------------------------------------------!
  integer :: row, col
!----------------------------------------------------------------------!

  do row=1,n
    do col=1,n
      b(col,row) = a(row,col)
    end do
  end do

  end subroutine Transpose_Matrix

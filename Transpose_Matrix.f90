!==============================================================================!
  subroutine Transpose_Matrix(b, a)
!------------------------------------------------------------------------------!
  implicit none
!------------------------------------------------------------------------------!
  real, dimension(:,:) :: b
  real, dimension(:,:) :: a
!------------------------------------------------------------------------------!
  integer :: row, col
!==============================================================================!

  do row=1, size(b,1)
    do col=1, size(b,2)
      b(col,row) = a(row,col)
    end do
  end do

  end subroutine Transpose_Matrix

!======================================================================!
  subroutine Print_Matrix(a, n)
!----------------------------------------------------------------------!
  implicit none
!----------------------------------------------------------------------!
  integer              :: n
  real, dimension(n,n) :: a
!----------------------------------------------------------------------!
  integer :: row, col
!----------------------------------------------------------------------!

  do row = 1, n
    do col = 1, n
      write(*,"(f8.3)",advance="no") a(row,col)
    end do       
    write(*,*) ""
  end do           

  end subroutine Print_Matrix

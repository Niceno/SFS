!======================================================================!
  subroutine Print_Matrix(a)
!----------------------------------------------------------------------!
  implicit none
!----------------------------------------------------------------------!
  real, dimension(:,:) :: a
!----------------------------------------------------------------------!
  integer :: row, col
!----------------------------------------------------------------------!

  do row = 1, size(a, 1)
    do col = 1, size(a, 2)
      write(*,"(f8.3)",advance="no") a(row,col)
    end do       
    write(*,*) ""
  end do           

  end subroutine Print_Matrix

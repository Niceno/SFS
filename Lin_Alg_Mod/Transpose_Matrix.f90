!==============================================================================!
  subroutine Lin_Alg_Mod_Transpose_Matrix(b, a)
!------------------------------------------------------------------------------!
!   Transposes a full matrix.                                                  !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  real, dimension(:,:) :: b
  real, dimension(:,:) :: a
!-----------------------------------[Locals]-----------------------------------!
  integer :: row, col
!==============================================================================!

  do row=1, size(b,1)
    do col=1, size(b,2)
      b(col,row) = a(row,col)
    end do
  end do

  end subroutine
